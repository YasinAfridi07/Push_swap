#include "../push_swap.h"

void	ra(t_stack *a)
{
	int	data[0];
	int	i;

	data[0] = a->data[0];
	i = 1;
	while (i < a->size)
	{
		a->data[i - 1] = a->data[i];
		i++;
	}
	a->data[i - 1] = data[0];
	ft_printf("ra\n");
}
